package CoreJava;

public class DataType1 {

	//main method()--> starting execution point of the program
	public static void main(String[] args) {

		//1. int (integer)
		int i=20;
		i=30; //assigning value to i
		int j=10;
		int k=40;
		int l=-1;
		int m=0;
		
		//2. double (floating values)
		double d=12.75;
		double d2=34.79;
		double d3=100;//100.00
		
		//3. char (Characters) Only single character value, should be written in single quotes.
		char c='a';
		char c1='A';
		char c2='$';
		char c3='1';
		
		//4. Boolean:
		Boolean b=true;
		Boolean b1=false;
		
		//5. String: is a class, not a data type. It has its own methods. It should be stored between double quotes
		String s="Hello";
		String s2="World";
		String s3="1100";
		String s4="11.35";
		String s5="true";



	}

}
